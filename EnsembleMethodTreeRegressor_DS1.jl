md"""
### Automatic tuning

So far we've manually specified one or two hyperparameter ranges and generate performance metrics for model selection. Can we pass the onerous duty to the computer? MLJ's TunedModel gives us a solution. 

Here's an example: we ask for a grid search for both "n\_subfeatures" and "bagging\_fraction", and let the machine find the best settings.
"""

# ╔═╡ e155b301-e324-4700-8e45-a6b555ba93a9
begin
	Random.seed!(1)
	r1 = range(forest, :(model.n_subfeatures), lower=1, upper=6)
	r2 = range(forest, :bagging_fraction, lower=0.4, upper=1.0)
	tm = TunedModel(model=forest, tuning=Grid(resolution=10),
	                resampling=CV(nfolds=6), ranges=[r1, r2],
	                measure=rms)
	m = machine(tm, X, y)
	MLJ.fit!(m);
end

# ╔═╡ 45ba3dc3-d8b0-4fd5-ab43-312162fda0b8
md"""
We can generate the report of the trained model (machine) and find the best performance obtained: 
"""

# ╔═╡ 26c2340d-cfc3-4c9d-b8a8-80cdcf610685
